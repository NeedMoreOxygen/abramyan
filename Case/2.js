const a = parseInt(prompt());

switch (a){
    case 1:
        console.log("Плохо");
        break;
    case 2:
        console.log("Неудовретворительно");
        break;
    case 3:
        console.log("Удовретворительно");
        break;
    case 4:
        console.log("Хорошо");
        break;
    case 5:
        console.log("Отлично");
        break;
    default:
        console.log("Ошибка.");
}